// ─── Thème (specs §11.2) ────────────────────────────────────────────────────────
export type ThemeMode = 'clair' | 'nocturne' | 'auto';

interface PrefsValue {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string) => string;
  theme: ThemeMode;
  setTheme: (m: ThemeMode) => void;
}

const PrefsContext = createContext<PrefsValue | null>(null);

function prefersDark(): boolean {
  return typeof window !== 'undefined' && window.matchMedia('(prefers-color-scheme: dark)').matches;
}

export function PrefsProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => (localStorage.getItem('admin_lang') as Lang) || 'fr');
  const [theme, setThemeState] = useState<ThemeMode>(() => (localStorage.getItem('admin_theme') as ThemeMode) || 'clair');

  // Applique/retire la classe `dark` sur <html> selon le thème (auto = suit l'OS).
  const applyTheme = useCallback((mode: ThemeMode) => {
    const dark = mode === 'nocturne' || (mode === 'auto' && prefersDark());
    document.documentElement.classList.toggle('dark', dark);
  }, []);

  useEffect(() => { applyTheme(theme); }, [theme, applyTheme]);

  // En mode auto, suivre les changements de l'OS en direct.
  useEffect(() => {
    if (theme !== 'auto') return;
    const mq = window.matchMedia('(prefers-color-scheme: dark)');
    const handler = () => applyTheme('auto');
    mq.addEventListener('change', handler);
    return () => mq.removeEventListener('change', handler);
  }, [theme, applyTheme]);

  const setLang = (l: Lang) => { setLangState(l); localStorage.setItem('admin_lang', l); };
  const setTheme = (m: ThemeMode) => { setThemeState(m); localStorage.setItem('admin_theme', m); };

  const t = useCallback(
    (key: string) => TRANSLATIONS[lang][key] ?? TRANSLATIONS.fr[key] ?? key,
    [lang]
  );

  return (
    <PrefsContext.Provider value={{ lang, setLang, t, theme, setTheme }}>
      {children}
    </PrefsContext.Provider>
  );
}

export function usePrefs(): PrefsValue {
  const ctx = useContext(PrefsContext);
  if (!ctx) throw new Error('usePrefs must be used within PrefsProvider');
  return ctx;
}

// Raccourci pratique : juste la fonction de traduction.
export function useT() { return usePrefs().t; }
